func InstallClaude(daemonAddr string) error {
	home, err := os.UserHomeDir()
	if err != nil {
		return err
	}
	claudeDir := filepath.Join(home, ".claude")
	if err := os.MkdirAll(claudeDir, 0o755); err != nil {
		return err
	}
	settingsPath := filepath.Join(claudeDir, "settings.json")
	cfgJSON, err := patchClaudeSettings(settingsPath, daemonAddr)
	if err != nil {
		return fmt.Errorf("patch claude settings: %w", err)
	}
	if err := os.WriteFile(settingsPath, cfgJSON, 0o644); err != nil {
		return fmt.Errorf("write claude settings: %w", err)
	}
	fmt.Println("Claude Code hooks installed.")
	fmt.Printf("  settings:  %s\n", settingsPath)
	fmt.Printf("  endpoint:  %s/api/v1/hooks/claude\n", daemonAddr)
	fmt.Println("\nStart the daemon with 'oc daemon', then open a Claude Code session. Messages will be recorded.")
	return nil
}


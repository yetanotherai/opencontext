func InstallShell(sensitivity int) error {
	if sensitivity < 1 || sensitivity > 2 {
		sensitivity = 1
	}
	home, err := os.UserHomeDir()
	if err != nil {
		return err
	}
	ocBin, err := resolvedExecutable()
	if err != nil {
		return err
	}
	hooksDir := filepath.Join(home, ".opencontext", "collectors", "shell")
	if err := os.MkdirAll(hooksDir, 0o755); err != nil {
		return err
	}
	zshHook := shellHookZsh(ocBin, sensitivity)
	bashHook := shellHookBash(ocBin, sensitivity)
	if err := os.WriteFile(filepath.Join(hooksDir, "hooks.zsh"), []byte(zshHook), 0o644); err != nil {
		return err
	}
	if err := os.WriteFile(filepath.Join(hooksDir, "hooks.bash"), []byte(bashHook), 0o644); err != nil {
		return err
	}
	appendIfMissing(filepath.Join(home, ".zshrc"), "\n# OpenContext shell collector\nsource ~/.opencontext/collectors/shell/hooks.zsh\n", "hooks.zsh")
	appendIfMissing(filepath.Join(home, ".bashrc"), "\n# OpenContext shell collector\nsource ~/.opencontext/collectors/shell/hooks.bash\n", "hooks.bash")

	shell := detectShell()
	fmt.Println("Shell hooks installed.")
	fmt.Printf("  hooks dir:   %s\n", hooksDir)
	fmt.Printf("  shell:       %s (zsh and bash configs were checked)\n", shell)
	fmt.Printf("  sensitivity: L%d\n", sensitivity)
	fmt.Println("\nRestart your shell or source your shell config.")
	return nil
}

func patchClaudeSettings(cfgPath, daemonAddr string) ([]byte, error) {
	endpoint := strings.TrimRight(daemonAddr, "/") + "/api/v1/hooks/claude"
	ocEntry := map[string]any{
		"hooks": []map[string]any{{
			"type":    "http",
			"url":     endpoint,
			"timeout": 2,
		}},
	}
	entryJSON, _ := json.Marshal(ocEntry)

	topLevel := map[string]json.RawMessage{}
	if data, err := os.ReadFile(cfgPath); err == nil {
		_ = json.Unmarshal(data, &topLevel)
	}
	hooksMap := map[string]json.RawMessage{}
	if raw, ok := topLevel["hooks"]; ok {
		_ = json.Unmarshal(raw, &hooksMap)
	}
	for _, eventName := range []string{"UserPromptSubmit", "SessionStart"} {
		hooksMap[eventName] = prependJSONEntry(hooksMap[eventName], entryJSON, endpoint)
	}
	hooksRaw, err := json.Marshal(hooksMap)
	if err != nil {
		return nil, err
	}
	topLevel["hooks"] = hooksRaw
	return json.MarshalIndent(topLevel, "", "  ")
}

func patchCodexHooks(cfgPath, scriptPath string) ([]byte, error) {
	existing := map[string]json.RawMessage{}
	if data, err := os.ReadFile(cfgPath); err == nil {
		_ = json.Unmarshal(data, &existing)
	}
	ocEntry := map[string]any{"hooks": []map[string]string{{"type": "command", "command": scriptPath}}}
	entryJSON, _ := json.Marshal(ocEntry)
	for _, eventName := range []string{"UserPromptSubmit", "SessionStart"} {
		existing[eventName] = prependJSONEntry(existing[eventName], entryJSON, scriptPath)
	}
	return json.MarshalIndent(existing, "", "  ")
}

func patchCursorHooks(cfgPath string) ([]byte, error) {
	ocEntry := map[string]string{"command": "./hooks/oc-capture.sh"}
	entryJSON, _ := json.Marshal(ocEntry)
	topLevel := map[string]json.RawMessage{}
	if data, err := os.ReadFile(cfgPath); err == nil {
		_ = json.Unmarshal(data, &topLevel)
	}
	topLevel["version"] = json.RawMessage(`1`)
	for _, key := range []string{"beforeSubmitPrompt", "sessionStart"} {
		delete(topLevel, key)
	}
	hooksMap := map[string]json.RawMessage{}
	if raw, ok := topLevel["hooks"]; ok {
		_ = json.Unmarshal(raw, &hooksMap)
	}
	for _, key := range []string{"beforeSubmitPrompt", "sessionStart"} {
		hooksMap[key] = prependJSONEntry(hooksMap[key], entryJSON, "oc-capture")
	}
	hooksRaw, err := json.Marshal(hooksMap)
	if err != nil {
		return nil, err
	}
	topLevel["hooks"] = hooksRaw
	return json.MarshalIndent(topLevel, "", "  ")
}

func patchOpenCodeHooks(cfgPath, scriptPath string) ([]byte, error) {
	return patchCodexHooks(cfgPath, scriptPath)
}

func prependJSONEntry(existing json.RawMessage, entry json.RawMessage, dedupKey string) json.RawMessage {
	var items []json.RawMessage
	if existing != nil {
		_ = json.Unmarshal(existing, &items)
	}
	filtered := []json.RawMessage{entry}
	for _, item := range items {
		if !strings.Contains(string(item), dedupKey) {
			filtered = append(filtered, item)
		}
	}
	out, _ := json.Marshal(filtered)
	return out
}

func resolvedExecutable() (string, error) {
	exe, err := os.Executable()
	if err != nil {
		return "", fmt.Errorf("resolve oc binary path: %w", err)
	}
	if resolved, err := filepath.EvalSymlinks(exe); err == nil {
		exe = resolved
	}
	return exe, nil
}

func appendIfMissing(path, content, marker string) {
	data, _ := os.ReadFile(path)
	if strings.Contains(string(data), marker) {
		return
	}
	f, err := os.OpenFile(path, os.O_CREATE|os.O_WRONLY|os.O_APPEND, 0o644)
	if err != nil {
		fmt.Fprintf(os.Stderr, "warning: could not update %s: %v\n", path, err)
		return
	}
	defer f.Close()
	_, _ = f.WriteString(content)
}

func detectShell() string {
	shell := os.Getenv("SHELL")
	if idx := strings.LastIndex(shell, "/"); idx >= 0 {
		return shell[idx+1:]
	}
	if shell != "" {
		return shell
	}
	return "sh"
}

func shellHookZsh(ocBin string, sensitivity int) string {
	return fmt.Sprintf(`# OpenContext shell hooks — installed by: oc collector shell install
# Re-run install to update.
# oc binary: %s  sensitivity: %d

_oc_preexec() {
  _oc_cmd_start=$(date +%%s%%3N)
  _oc_cmd_input=$1
}

_oc_skip_cmd() {
  local cmd=$1
  [[ "$cmd" == " "* ]] && return 0
  local first="${cmd%%%% *}"
  case "$first" in
    clear|reset|ls|ll|la|pwd|cd|history|exit)
      [[ "$cmd" == "$first" ]] && return 0 ;;
  esac
  return 1
}

_oc_precmd() {
  local _oc_exit=$?
  local _oc_end
  _oc_end=$(date +%%s%%3N)
  local _oc_dur=$(( _oc_end - ${_oc_cmd_start:-$_oc_end} ))
  [[ -z "$_oc_cmd_input" ]] && return 0
  _oc_skip_cmd "$_oc_cmd_input" && { _oc_cmd_input=""; return 0; }
  %s collector shell push --command "$_oc_cmd_input" --exit-code "$_oc_exit" --duration-ms "$_oc_dur" --cwd "$PWD" --sensitivity %d &>/dev/null &!
  _oc_cmd_input=""
}

autoload -Uz add-zsh-hook
add-zsh-hook preexec _oc_preexec
add-zsh-hook precmd _oc_precmd
`, ocBin, sensitivity, ocBin, sensitivity)
}

func shellHookBash(ocBin string, sensitivity int) string {
	return fmt.Sprintf(`# OpenContext shell hooks — installed by: oc collector shell install
# oc binary: %s  sensitivity: %d

_oc_preexec() {
  _oc_cmd_start=$(date +%%s%%3N 2>/dev/null || echo 0)
  _oc_cmd_input=$BASH_COMMAND
}

_oc_skip_cmd() {
  local cmd=$1
  [[ "$cmd" == " "* ]] && return 0
  local first="${cmd%%%% *}"
  case "$first" in
    clear|reset|ls|ll|la|pwd|cd|history|exit)
      [[ "$cmd" == "$first" ]] && return 0 ;;
  esac
  return 1
}

_oc_precmd() {
  local _oc_exit=$?
  local _oc_end
  _oc_end=$(date +%%s%%3N 2>/dev/null || echo 0)
  local _oc_dur=$(( _oc_end - ${_oc_cmd_start:-0} ))
  [[ -z "$_oc_cmd_input" ]] && return 0
  [[ "$_oc_cmd_input" == "_oc_precmd" ]] && return 0
  _oc_skip_cmd "$_oc_cmd_input" && { _oc_cmd_input=""; return 0; }
  ( %s collector shell push --command "$_oc_cmd_input" --exit-code "$_oc_exit" --duration-ms "$_oc_dur" --cwd "$PWD" --sensitivity %d &>/dev/null & )
  _oc_cmd_input=""
}

trap '_oc_preexec "$BASH_COMMAND"' DEBUG
PROMPT_COMMAND="_oc_precmd${PROMPT_COMMAND:+;$PROMPT_COMMAND}"
`, ocBin, sensitivity, ocBin, sensitivity)
}
